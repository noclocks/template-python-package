"""Addition Module
Module for simple addition (math) logic.
"""

def add_one(number: int) -> int:
    """Add One
    Args:
        number (int): The number to add one to
    Returns:
        int
    """
    return number + 1


def add_n(number: int, n: int) -> int:
    """Add N
    Args:
        number (int): The number to add N to
        n (int): The value to add to number.
    Returns:
        int
    """
    return number + n
